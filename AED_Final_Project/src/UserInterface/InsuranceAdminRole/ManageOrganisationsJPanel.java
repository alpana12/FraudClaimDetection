    public void populateOrganizationTypeComboBox(){
        organizationTypeJComboBox.removeAllItems();
        if(enterprise instanceof InsuranceEnterprise){
            for(Organization.Type type : Organization.Type.values()){
                if((!type.getValue().equals(Organization.Type.InsuranceAdmin.getValue())) && (!type.getValue().equals(Organization.Type.HospitalAdmin.getValue())) ){
                    organizationTypeJComboBox.addItem(type);
                }
            }
        }        
    }
    
        public void populateOrganizationTable(){
        DefaultTableModel model = (DefaultTableModel)organizationJTable.getModel();
        model.setRowCount(0);
        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            Object[] row = new Object[1];
            row[0] = organization.getName();
            model.addRow(row);
        }
    }
