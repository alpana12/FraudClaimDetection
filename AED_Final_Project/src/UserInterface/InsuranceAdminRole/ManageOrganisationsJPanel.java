    private void addOrganisationJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrganisationJButtonActionPerformed

         try{
            boolean alreadyPresent = false;
            Organization.Type type = (Organization.Type) organizationTypeJComboBox.getSelectedItem();
            if(enterprise.getOrganizationDirectory().getOrganizationList().isEmpty()){
                enterprise.getOrganizationDirectory().createOrganization(type);
                populateOrganizationTable();
            }else{
                for(Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){
                    if(org.getName().equalsIgnoreCase(type.getValue())){
                        alreadyPresent = true;
                        JOptionPane.showMessageDialog(null, "Organization is already added", "Warning", JOptionPane.WARNING_MESSAGE);
                        break;
                    }
                }
                if(!alreadyPresent){
                        enterprise.getOrganizationDirectory().createOrganization(type);
                        populateOrganizationTable();
                    }
                
            }
            
        }catch(Exception e){
            System.out.println(e);
        }
        
       
    }//GEN-LAST:event_addOrganisationJButtonActionPerformed


